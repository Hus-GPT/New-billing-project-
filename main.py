from data_layer import DataLayer
from business_logic import BusinessLogic
from ai_layer import AILayer
from presentation_layer import PresentationLayer

def seed_demo_data():
    """
    دالة منفصلة لشحن وتعبئة النظام ببيانات تجريبية نموذجية واسعة.
    لا يتم تشغيلها تلقائياً عند بدء النظام، بل تترك للمستخدم لاستدعائها يدوياً للاختبار.
    """
    print("[الملف الرئيسي] جاري شحن وتصفير البيانات بالقراءات والمستأجرين التجريبيين...")
    db = DataLayer()
    db.load_demo_data()
    print("[الملف الرئيسي] تم شحن البيانات التجريبية وحفظها في data_backup بنجاح!")

def run_app():
    """
    تشغيل التطبيق الرئيسي: تحميل البيانات المتاحة، وتهيئة منطق الأعمال، وعرض لوحة التحكم المرئية.
    """
    print("==========================================================")
    print("      مرحباً بك في نظام سند الرقمي للإدارة العقارية       ")
    print("==========================================================")
    
    # 1. تهيئة طبقة البيانات والتحميل التلقائي للأرشيف
    db = DataLayer()
    
    # 2. تهيئة طبقة منطق الأعمال والربط بقاعدة البيانات
    bl = BusinessLogic(db)
    
    # 3. تهيئة طبقة العرض والواجهات التفاعلية
    pl = PresentationLayer(db)
    
    # 4. تصفية وفحص الفواتير غير الصادرة للمستأجرين لتنبيه المستخدم
    unissued = bl.find_unissued_bills()
    if unissued:
        print(f"[تنبيه تشغيلي] يوجد عدد ({len(unissued)}) مستأجر نشط بدون فواتير صادرة حالياً:")
        for t in unissued:
            print(f"  - المستأجر: {t['name']} | الشقة: {t['apartment_number']}")
    else:
        print("[فحص النظام] جميع المستأجرين النشطين يمتلكون فواتير صادرة ومقيدة.")
        
    # 5. جلب وعرض تقرير الأداء المالي السريع في سطر الأوامر
    report = bl.generate_monthly_report()
    print("\n[تقرير الأداء المالي الحالي]:")
    print(f"  • إجمالي قيمة الفواتير: {report['total_invoiced']} ر.ي")
    print(f"  • المبالغ المحصلة فعلياً: {report['collected_amount']} ر.ي")
    print(f"  • المستحقات المتبقية في ذمة العملاء: {report['pending_amount']} ر.ي")
    print(f"  • نسبة التحصيل الحالية: {bl.generate_monthly_report().get('paid_bills_count', 0)} فواتير مدفوعة بالكامل.")
    print("----------------------------------------------------------")

    # 6. عرض لوحة التحكم التفاعلية للمستخدم
    pl.render_dashboard()

def run_ai_ocr_simulation(image_path, tenant_id, bill_type):
    """
    محاكاة متكاملة للربط بين الذكاء الاصطناعي (OCR) ومنطق الأعمال والبيانات لإصدار فاتورة جديدة.
    """
    db = DataLayer()
    ai = AILayer(db)
    bl = BusinessLogic(db)
    
    print(f"\n--- بدء محاكاة القراءة الآلية للعداد للعميل ({tenant_id}) لخدمة ({bill_type}) ---")
    
    # 1. قراءة العداد بالذكاء الاصطناعي
    reading_val = ai.read_meter_from_image(image_path)
    
    # 2. التحقق من صحة القراءة منطقياً ومقارنتها بالقراءات السابقة
    is_valid, msg = ai.validate_reading_logic(tenant_id, bill_type, reading_val)
    print(f"[فحص الذكاء الاصطناعي]: {msg}")
    
    if is_valid:
        # 3. إصدار الفاتورة تلقائياً في حال كانت القراءة مطابقة للضوابط
        bill_id = bl.issue_utility_bill(tenant_id, bill_type, reading_val)
        print(f"[نجاح المحاكاة] تم تسجيل القراءة وإصدار الفاتورة الجديدة بنجاح بالرقم: {bill_id}")
    else:
        print("[فشل المحاكاة] تم رفض القراءة وحظر إصدار الفاتورة نظراً لعدم اجتياز معايير الصحة.")

if __name__ == "__main__":
    # تشغيل التطبيق افتراضياً عند استدعاء الملف مباشرة
    run_app()
