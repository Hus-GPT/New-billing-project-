   # سيقوم النظام بقراءة صورة عداد افتراضية، والتحقق من القراءة منطقياً، وإصدار فاتورة مياه جديدة للمستأجر T101
   run_ai_ocr_simulation("meter_photo.png", "T101", "مياه")
